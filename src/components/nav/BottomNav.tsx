export function BottomNav() {
  const pathname = usePathname();

  // Hide nav during session or onboarding
  if (pathname === "/session" || pathname === "/onboarding") return null;

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50" style={{ background: "var(--surface)", borderTop: "1px solid var(--border)" }}>
      <div className="flex items-center justify-around px-2 pb-safe" style={{ paddingBottom: "max(12px, env(safe-area-inset-bottom))", paddingTop: "8px" }}>
        {navItems.map((item) => {
          const active = pathname === item.href;
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                "flex flex-col items-center gap-1 px-3 py-1 rounded-xl transition-all duration-150",
                active ? "text-amber-400" : "text-muted hover:text-slate-300"
              )}
              style={{ color: active ? "var(--focus)" : "var(--text-muted)" }}
            >
              {item.icon(active)}
              <span className="text-[10px] font-medium tracking-wide">{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
